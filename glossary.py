def glossary_for_prompt() -> str:
    """Возвращает словарь в виде строки для вставки в промпт ИИ."""
    lines = []
    for entry in GLOSSARY:
        bad = entry["bad"]
        good = entry["good"]
        comment = entry.get("comment", "")
        if bad == good:
            continue  # пропускаем «верно» записи
        line = f'  • Неверно: «{bad}» → Верно: «{good}»'
        if comment:
            line += f"  ({comment})"
        lines.append(line)
    return "\n".join(lines)
